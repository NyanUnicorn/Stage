			<h3 class="SousParties"><a name="Techniques_de_soins_utilisees">Techniques de soins utilisées</a></h3>

			<p>Dans le monde de la chiropraxie, il existe un grand nombre de techniques de soins qui peuvent être utilisées dans le but d’être encore plus polyvalent dans l’approche des soins manuelles.
				Les formations post-graduate sont très nombreuses et les approches sont très diverses.
				C’est pourquoi votre chiropracteur s’est déjà formé à certaines techniques particulières, et continue de se former au fil des ans.
				Les techniques de soins utilisées ou en cours de formation et leur description :
			</p>

			<ul>
				<li>- Spécialisation dans le traitement des dysfonctionnements mécaniques des sportifs. A travers plusieurs type de séminaires différents et de part son expérience, votre chiropracteur s’est dirigé vers une pratique concernant les sportifs et il cherche à optimiser au maximum les performances physiques de ces derniers.</li>
				<li>- Sacro Occipital Technique, Technique basée sur le la relation entre le bassin et l’occiput (base du crâne), elle permet un fonctionnement optimal de tout le rachis et du corps dans sa globalité, grâce, entre autres, à l’utilisation de blocs placés sous le bassin.</li>
				<li>- Kinésiologie Appliquée, La kinésiologie est un concept qui a été inventé par un chiropracteur Georges Goodheart, cette méthode consiste à analyser et comprendre les différents mécanismes de défenses du corps humains sur le plan fonctionnel, émotionnel et nutritif, par le biais de tests musculaires.</li>
				<li> - Technique Gonstead, Clarence Gonstead fût l’un des plus grands chiropracteurs de l’histoire de la profession. Dans les années 1960, il bâtît l’un des plus grands centres de soins aux Etats Unis consacré au traitement des patients, la clinique Gonstead, encore ouverte aujourd’hui. Cette technique est basée sur des ajustements précis pour libérer les tensions sur les disques intervertébraux.</li>
				<li>- Taping, C’est une méthode basée sur les tensions musculaires et ligamentaires, le praticien utilise lorsque nécessaire des bandes de Tape qui collent à la peau pour optimiser, entre autres, le fonctionnement d’un muscle ou d’une articulation par le biais des mécano récepteurs sous cutanées.</li>
				<li>- Technique Speeder, le Speeder est un outil chiropratique qui permet l’ajustement des extrémités dans un confort optimal pour le patient. Le Speeder absorbe les chocs lors des ajustements sans en diminuer l’action principale.</li>
				<li>- Technique énergétique, votre chiropracteur est susceptible de travailler d’une manière plus énergétique sur votre corps, par le biais de visualisation et de travail extra sensoriel.</li>

			</ul>

