				<p>Qui peut consulter un chiropracteur ?</p>

				<p>La chiropraxie est indiquée pour tous, sans distinction d’âge. Cependant, certaines périodes de la vie, certains métiers ou certains sports sont plus propices à une « surutilisation » des articulations, muscles ou tendons comme à l’adoption de gestes et postures inadaptés.</p>

