				<h3 class="SousParties"><a name="Deroulement_d'une_séance">Déroulement d’une séance</a></h3>

				<p>Comment se déroule la consultation ?</p>

				<p>Une consultation chiropratique se déroule en trois étapes :</p>

				<ul>

					<li>Détecter :</li>

						<p>Le chiropracteur commence par vous interroger sur votre santé, vos antécédents médicaux et sur les raisons de votre consultation.</p>
						<p>Il s’intéresse également à ce qui, dans vos conditions de vie et de travail, peut favoriser l’émergence de troubles.</p>
						<p>Il effectue ensuite un examen physique et peut, autant que de besoin, solliciter des examens complémentaires, radiologiques notamment.</p>
						<p>Sur cette base, le chiropracteur définit son plan de traitement et vous indique le nombre de consultations qu’il juge nécessaire à l’amélioration de votre état.</p>

					<li>Corriger :</li>

						<p>Le chiropracteur intervient manuellement et réalise des actes de manipulation de la colonne vertébrale et des articulations (dits ajustements chiropratiques).</p>
						<p>Le chiropracteur intervient également sur les tissus mous (muscles, tendons et ligaments).</p>
						<p>Il peut aussi avoir recours à des tables et appareils spécialisés.</p>

					<li>Prévenir :</li>

						<p>Le chiropracteur peut également vous donner des conseils en termes de postures, de nutrition ou d’hygiène de vie de nature à renforcer l’efficacité du traitement.</p>
					<li>Ressent-on des effets secondaires ?</li>

					<p>La majorité des patients ressentent un soulagement immédiat à l’issue du traitement. Toutefois, il peut arriver que certains ressentent des courbatures ou une douleur légère, semblables à celles ressenties après un entraînement sportif. 10 % des patients témoignent de maux de tête post-manipulation. Ces réactions bénignes disparaissent spontanément en 24 à 48 heures.</p>

				</ul>

